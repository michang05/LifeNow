package com.lifenow.pojo;

import android.widget.CheckBox;
import android.widget.TextView;



public class SourceNameItem {

	TextView sourceName;
	CheckBox checked;
	
	public SourceNameItem(TextView sourceName,CheckBox checked) {
		this.sourceName=sourceName;
		this.checked=checked;
	}
	
	public TextView getSourceName() {
		return sourceName;
	}
	public void setSourceName(TextView sourceName) {
		this.sourceName = sourceName;
	}
	public CheckBox getChecked() {
		return checked;
	}
	public void setChecked(CheckBox checked) {
		this.checked = checked;
	}
	

	
}
